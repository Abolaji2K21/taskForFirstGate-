package ChapterTwo;

public class Arithmetic {


    public static int  sumOfSquares(int firstInput,int secondInput){
        int firstSquare = firstInput * firstInput;
        int secondSquare = secondInput * secondInput;

        return firstSquare + secondSquare;
    }

    public static int differenceOfSquares(int firstInput, int secondInput){
        int firstSquare = firstInput * firstInput;
        int secondSquare = secondInput * secondInput;

        return firstSquare - secondSquare;
    }

}
